  private void loadEvenements() {
    evenements.removeAllItems();
    for (Evenement e : evenementService.findAll()) {
        evenements.addItem(e);  // Objet directement
    }
}

private void loadParticipants() {
    participants.removeAllItems();
    for (Participant p : participantService.findAll()) {
        participants.addItem(p);  // Objet directement
    }
}

private void loadInscriptions() {
    model.setRowCount(0);
    for (Inscription i : inscriptionService.findAll()) {
        model.addRow(new Object[]{
            i.getId(),
            i.getEvenement().getTitre(),
            i.getParticipant().getNom(),
            i.getParticipant().getEmail(),
            i.getDateInscription()
        });
    }
}

private void clearFields() {
    evenements.setSelectedIndex(0);
    participants.setSelectedIndex(0);
    dateIN.setDate(new Date());
    updateTauxRemplissage();
}
private void updateTauxRemplissage() {
    if (evenements.getSelectedItem() != null) {
        Evenement evenementSelectionne = (Evenement) evenements.getSelectedItem(); 
        
        int taux = inscriptionService.tauxRemplissage(evenementSelectionne.getId_evenement());
        tauxLabel.setText("Taux de remplissage: " + taux + "%");
        
        if (taux < 50) {
            tauxLabel.setForeground(new java.awt.Color(0, 153, 0));
        } else if (taux < 80) {
            tauxLabel.setForeground(new java.awt.Color(255, 153, 0));
        } else if (taux < 100) {
            tauxLabel.setForeground(new java.awt.Color(204, 0, 0));
        } else {
            tauxLabel.setForeground(new java.awt.Color(153, 0, 0));
            tauxLabel.setText("Taux de remplissage: " + taux + "% - COMPLET!");
        }
    }
}


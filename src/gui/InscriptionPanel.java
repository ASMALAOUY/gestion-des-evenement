    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
      try {
        int selectedRow = inscription.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner une inscription à supprimer!", "Avertissement", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirmation = JOptionPane.showConfirmDialog(this, "Êtes-vous sûr de vouloir supprimer cette inscription?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (confirmation == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
            Inscription inscriptionASupprimer = inscriptionService.findById(id);

            if (inscriptionASupprimer != null && inscriptionService.delete(inscriptionASupprimer)) {
                JOptionPane.showMessageDialog(this, "Inscription supprimée avec succès!", "Succès", JOptionPane.INFORMATION_MESSAGE);
                loadInscriptions();
                updateTauxRemplissage();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la suppression!", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        }
    } catch (NumberFormatException nfe) {
        JOptionPane.showMessageDialog(this, "Erreur de format des données!", "Erreur", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur inattendue: " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_supprimerActionPerformed



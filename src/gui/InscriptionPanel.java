    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
         int selectedRow = inscription.getSelectedRow();
        
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, 
                "Veuillez sélectionner une inscription à modifier!", 
                "Avertissement", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Vérifier que tous les champs sont remplis
        if (evenements.getSelectedItem() == null || participants.getSelectedItem() == null || dateIN.getDate() == null) {
            JOptionPane.showMessageDialog(this, 
                "Veuillez remplir tous les champs!", 
                "Erreur", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        int id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        
        // Récupérer l'événement
        String evenementNom = evenements.getSelectedItem().toString();
        Evenement evenement = null;
        for (Evenement e : evenementService.findAll()) {
            if (e.getTitre().equals(evenementNom)) {
                evenement = e;
                break;
            }
        }

        // Récupérer le participant
        String participantInfo = participants.getSelectedItem().toString();
        String participantNom = participantInfo.split(" - ")[0];
        Participant participant = null;
        for (Participant p : participantService.findAll()) {
            if (p.getNom().equals(participantNom)) {
                participant = p;
                break;
            }
        }

        Date dateInscription = dateIN.getDate();

        Inscription inscriptionModifiee = new Inscription(id, evenement, participant, dateInscription);
        
        if (inscriptionService.update(inscriptionModifiee)) {
            JOptionPane.showMessageDialog(this, 
                "Inscription modifiée avec succès!", 
                "Succès", 
                JOptionPane.INFORMATION_MESSAGE);
            loadInscriptions();
            updateTauxRemplissage(); // IMPORTANT: Mettre à jour le taux
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, 
                "Erreur lors de la modification!", 
                "Erreur", 
                JOptionPane.ERROR_MESSAGE);
        }
    
        
    }//GEN-LAST:event_modifierActionPerformed

    private void inscrireActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inscrireActionPerformed
             try {
        Date dateInscription = dateIN.getDate();
        Participant participant = (Participant) participants.getSelectedItem();
        Evenement evenement = (Evenement) evenements.getSelectedItem();

        if (dateInscription == null || participant == null || evenement == null) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs!", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean success = inscriptionService.create(new Inscription(evenement, participant, dateInscription));

        if (success) {
            JOptionPane.showMessageDialog(this, "Inscription ajoutée avec succès!", "Succès", JOptionPane.INFORMATION_MESSAGE);
            loadInscriptions();
            updateTauxRemplissage();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout de l'inscription!", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    } catch (NumberFormatException nfe) {
        JOptionPane.showMessageDialog(this, "Erreur de format des données!", "Erreur", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur inattendue: " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_inscrireActionPerformed

    private void inscriptionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_inscriptionMouseClicked

    int selectedRow = inscription.getSelectedRow();
    if (selectedRow != -1) {
        String evenementNom = model.getValueAt(selectedRow, 1).toString();
        String participantNom = model.getValueAt(selectedRow, 2).toString();
        Date dateInsc = (Date) model.getValueAt(selectedRow, 4);
        
        // Chercher l'événement dans le combo
        for (int i = 0; i < evenements.getItemCount(); i++) {
            Evenement e = (Evenement) evenements.getItemAt(i);
            if (e.getTitre().equals(evenementNom)) {
                evenements.setSelectedIndex(i);
                break;
            }
        }
        
        // Chercher le participant dans le combo
        for (int i = 0; i < participants.getItemCount(); i++) {
            Participant p = (Participant) participants.getItemAt(i);
            if (p.getNom().equals(participantNom)) {
                participants.setSelectedIndex(i);
                break;
            }
        }
        
        dateIN.setDate(dateInsc);
    }

            
    }//GEN-LAST:event_inscriptionMouseClicked


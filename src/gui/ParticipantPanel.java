    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
      int selectedRow = participants.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un participant à supprimer");
        return;
    }
    
    int reponse = JOptionPane.showConfirmDialog(this, 
        "Voulez-vous vraiment supprimer cet participant?", 
        "Confirmation", 
        JOptionPane.YES_NO_OPTION);
    
    if (reponse == JOptionPane.YES_OPTION) {
        int id = (int) model.getValueAt(selectedRow, 0);
        Participant eventToDelete = ps.findById(id);  // Call through ev service
        if (eventToDelete != null) {
            ps.delete(eventToDelete);
            loadParticipants();
            clearFields();
            JOptionPane.showMessageDialog(this, "Participant supprimé avec succès!");
        } else {
            JOptionPane.showMessageDialog(this, "Erreur: Participant introuvable!");
        }
    }
    }//GEN-LAST:event_supprimerActionPerformed

    private void ModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifierActionPerformed
       int selectedRow = participants.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un  participant à modifier");
        return ;
    }
    
    int id = (int) model.getValueAt(selectedRow, 0);
    String Nom = nom.getText();
    String email = emailtxt.getText();
    String organisation = Organisationtxt.getText();
    
    Participant e = new Participant(Nom, email, organisation);
    e.setId_participant(id);
    ps.update(e);
    loadParticipants();
    clearFields();
    }//GEN-LAST:event_ModifierActionPerformed


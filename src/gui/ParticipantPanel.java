    private void ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterActionPerformed
             try {
        String nom1 = nom.getText().trim();
        String email = emailtxt.getText().trim();
        String organisation = Organisationtxt.getText().trim();

        if (nom1.isEmpty() || email.isEmpty() || organisation.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs!", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean success = ps.create(new Participant(nom1, email, organisation));
        if (success) {
            JOptionPane.showMessageDialog(this, "Participant ajouté avec succès!", "Succès", JOptionPane.INFORMATION_MESSAGE);
            loadParticipants();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout du participant!", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur inattendue: " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_ajouterActionPerformed


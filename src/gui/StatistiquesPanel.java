    private void btnFiltrerLieuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrerLieuActionPerformed
                                              
    String lieu = filtreTexte.getText().trim();
    
    if (lieu.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Veuillez entrer un lieu!", 
            "Attention", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    evenementsFiltres = evenementService.findByLieu(lieu);
    
    if (evenementsFiltres.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Aucun événement trouvé pour ce lieu: " + lieu, 
            "Information", 
            JOptionPane.INFORMATION_MESSAGE);
        evenementsFiltres = new ArrayList<>();
    } else {
        JOptionPane.showMessageDialog(this, 
            evenementsFiltres.size() + " événement(s) trouvé(s) pour le lieu: " + lieu, 
            "Résultat", 
            JOptionPane.INFORMATION_MESSAGE);
    }
    
    updateTable();

    }//GEN-LAST:event_btnFiltrerLieuActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Date date = jDateChooser1.getDate();
    
    if (date == null) {
        JOptionPane.showMessageDialog(this, 
            "Veuillez sélectionner une date!", 
            "Attention", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    evenementsFiltres = evenementService.findByDate(date);
    
    if (evenementsFiltres.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Aucun événement trouvé pour cette date!", 
            "Information", 
            JOptionPane.INFORMATION_MESSAGE);
        evenementsFiltres = new ArrayList<>();
    } else {
        JOptionPane.showMessageDialog(this, 
            evenementsFiltres.size() + " événement(s) trouvé(s) pour cette date!", 
            "Résultat", 
            JOptionPane.INFORMATION_MESSAGE);
    }
    
    updateTable();
    }//GEN-LAST:event_jButton1ActionPerformed



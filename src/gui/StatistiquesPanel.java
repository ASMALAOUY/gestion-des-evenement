    private void updateTable() {
       model.setRowCount(0);
    for (Evenement e : evenementsFiltres) {
        int nbInscriptions = inscriptionService.countByEvenement(e.getId_evenement());
        int taux = inscriptionService.tauxRemplissage(e.getId_evenement());
        
        model.addRow(new Object[]{
            e.getId_evenement(),
            e.getTitre(),
            e.getLieu(),
            e.getDateEv(),
            e.getCapacite(),
            nbInscriptions,
            taux + "%"
        });
    }}

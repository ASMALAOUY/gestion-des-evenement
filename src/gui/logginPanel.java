    private void oubliepasswdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_oubliepasswdActionPerformed
String email = JOptionPane.showInputDialog(this, "Entrez votre email pour réinitialiser le mot de passe :");

    if (email != null && !email.isEmpty()) {
        UserService us = new UserService();

        if (us.checkUserExists(email)) {
            us.resetPassword(email);
            JOptionPane.showMessageDialog(this,
                    "Un nouveau mot de passe a été envoyé à votre email.",
                    "Succès",
                    JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            JOptionPane.showMessageDialog(this, "Email introuvable !");
        }
    }
    }//GEN-LAST:event_oubliepasswdActionPerformed


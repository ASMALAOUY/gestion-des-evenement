private void login() {
    String email = username.getText();
    String password = new String(psswd.getPassword());

    if(email.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez saisir l'email et le mot de passe !");
        return;
    }

    UserService us = new UserService();

    if(us.checkLogin(email, password)) {
        JOptionPane.showMessageDialog(this, "Connexion réussie !");

        JFrame parent = (JFrame) SwingUtilities.getWindowAncestor(this);
        parent.dispose();

        // Ouvrir ta vraie fenêtre principale
        Mainfram mf = new Mainfram();
        mf.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "Email ou mot de passe incorrect !");
    }
}


        
    

